package com.practice.pluralsightCourse;

public class M3City {

	private String name;

	public M3City(String name) {
		super();
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	@Override
	public String toString() {
		return "M3City [name=" + name + "]";
	}
	
	
}
